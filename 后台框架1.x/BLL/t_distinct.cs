		/// <summary>
		/// 获得数据列表
		/// </summary>
		public List<hm.Model.t_distinct> DataTableToList(DataTable dt)
		{
			List<hm.Model.t_distinct> modelList = new List<hm.Model.t_distinct>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				hm.Model.t_distinct model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = new hm.Model.t_distinct();
					if(dt.Rows[n]["id"]!=null && dt.Rows[n]["id"].ToString()!="")
					{
						model.id=int.Parse(dt.Rows[n]["id"].ToString());
					}
					if(dt.Rows[n]["code"]!=null && dt.Rows[n]["code"].ToString()!="")
					{
					model.code=dt.Rows[n]["code"].ToString();
					}
					if(dt.Rows[n]["name"]!=null && dt.Rows[n]["name"].ToString()!="")
					{
					model.name=dt.Rows[n]["name"].ToString();
					}
					if(dt.Rows[n]["cityCode"]!=null && dt.Rows[n]["cityCode"].ToString()!="")
					{
					model.cityCode=dt.Rows[n]["cityCode"].ToString();
					}
					modelList.Add(model);
				}
			}
			return modelList;
		}

